@router.get(
    "/me/governance-settings/diff",
    response_model=OrganizationGovernanceSettingsDiffResponse,
)
def get_organization_governance_settings_diff(
    from_version: int,
    to_version: int,
    db: Session = Depends(get_db),
    organization: Organization = Depends(get_current_organization),
    _=Depends(require_permission("org:read")),
) -> OrganizationGovernanceSettingsDiffResponse:
    if from_version < 1 or to_version < 1:
        raise HTTPException(status_code=status.HTTP_400_BAD_REQUEST, detail="from_version and to_version must be >= 1")
    if from_version > to_version:
        raise HTTPException(status_code=status.HTTP_400_BAD_REQUEST, detail="from_version must be <= to_version")

    target_versions = [from_version] if from_version == to_version else [from_version, to_version]
    rows = db.execute(
        select(OrganizationGovernanceSettingHistory)
        .where(
            OrganizationGovernanceSettingHistory.organization_id == organization.id,
            OrganizationGovernanceSettingHistory.version.in_(target_versions),
        )
        .order_by(OrganizationGovernanceSettingHistory.version.asc())
    ).scalars().all()
    if from_version == to_version:
        if len(rows) != 1 or rows[0].version != from_version:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Requested history versions were not found")
        from_row = rows[0]
        to_row = rows[0]
    else:
        if len(rows) != 2 or rows[0].version != from_version or rows[1].version != to_version:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Requested history versions were not found")
        from_row = rows[0]
        to_row = rows[1]

    full_rows = db.execute(
        select(OrganizationGovernanceSettingHistory)
        .where(
            OrganizationGovernanceSettingHistory.organization_id == organization.id,
            OrganizationGovernanceSettingHistory.version >= from_version,
            OrganizationGovernanceSettingHistory.version <= to_version,
        )
        .order_by(OrganizationGovernanceSettingHistory.version.asc())
    ).scalars().all()
    if not full_rows:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Requested history versions were not found")

    before_json = from_row.after_json or from_row.before_json or {}
    after_json = to_row.after_json or {}
    changed_keys = sorted(key for key in set(before_json.keys()) | set(after_json.keys()) if before_json.get(key) != after_json.get(key))

    return OrganizationGovernanceSettingsDiffResponse(
        from_version=from_version,
        to_version=to_version,
        changed_keys=changed_keys,
        before_json=before_json,
        after_json=after_json,
        entries_compared=len(full_rows),
        caveat=GOVERNANCE_EVIDENCE_CAVEAT,
    )


