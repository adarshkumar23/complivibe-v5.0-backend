@router.post(
    "/me/governance-settings/apply-to-open-batch-runs",
    response_model=OrganizationGovernanceApplyToOpenBatchRunsResponse,
)
def apply_governance_settings_to_open_batch_runs(
    payload: OrganizationGovernanceApplyToOpenBatchRunsRequest,
    request: Request,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_active_user),
    organization: Organization = Depends(get_current_organization),
    _=Depends(require_permission("org:update")),
) -> OrganizationGovernanceApplyToOpenBatchRunsResponse:
    reason = payload.reason.strip()
    if not reason:
        raise HTTPException(status_code=status.HTTP_400_BAD_REQUEST, detail="reason is required")

    settings_row = _get_governance_settings(db, organization.id)
    target_value = bool(settings_row.batch_cancellation_requires_approval) if settings_row is not None else False

    rows = db.execute(
        select(FrameworkReviewBatchAssignmentRun).where(
            FrameworkReviewBatchAssignmentRun.organization_id == organization.id
        )
    ).scalars().all()

    eligible_rows: list[FrameworkReviewBatchAssignmentRun] = []
    skipped_reasons: dict[str, int] = {
        "status_applied": 0,
        "status_cancelled": 0,
        "status_archived": 0,
        "has_created_assignments": 0,
        "already_matches_target": 0,
    }
    for row in rows:
        if row.status == "applied":
            skipped_reasons["status_applied"] += 1
            continue
        if row.status == "cancelled":
            skipped_reasons["status_cancelled"] += 1
            continue
        if row.status == "archived":
            skipped_reasons["status_archived"] += 1
            continue
        if int(row.created_assignments_count or 0) > 0:
            skipped_reasons["has_created_assignments"] += 1
            continue
        if bool(row.cancellation_requires_approval) == target_value:
            skipped_reasons["already_matches_target"] += 1
            continue
        eligible_rows.append(row)

    affected_run_ids = [row.id for row in eligible_rows]
    eligible_count = len(eligible_rows)
    skipped_count = len(rows) - eligible_count

    prior_values = {"true": 0, "false": 0}
    for item in eligible_rows:
        prior_values["true" if bool(item.cancellation_requires_approval) else "false"] += 1

    updated_count = 0
    if not payload.dry_run:
        for row in eligible_rows:
            row.cancellation_requires_approval = target_value
            updated_count += 1
        db.flush()

        audit_row = AuditService(db).write_audit_log(
            action="organization_governance_settings.applied_to_open_batch_runs",
            entity_type="organization_governance_setting",
            entity_id=settings_row.id if settings_row is not None else None,
            organization_id=organization.id,
            actor_user_id=current_user.id,
            after_json={
                "target_value": target_value,
                "eligible_count": eligible_count,
                "updated_count": updated_count,
                "affected_run_ids": [str(run_id) for run_id in affected_run_ids],
            },
            metadata_json={
                "source": "api",
                "reason": reason,
                "dry_run": False,
                "skipped_reasons": skipped_reasons,
            },
            ip_address=request.client.host if request.client else None,
            user_agent=request.headers.get("user-agent"),
        )
        _write_governance_history(
            db,
            organization_id=organization.id,
            event_type="open_batch_runs_rollout",
            setting_key="batch_cancellation_rollout",
            reason=reason,
            changed_by_user_id=current_user.id,
            before_json={"eligible_run_prior_values": prior_values},
            after_json={
                "target_value": target_value,
                "eligible_count": eligible_count,
                "updated_count": updated_count,
            },
            affected_entity_type="framework_review_batch_assignment_run",
            affected_entity_ids=affected_run_ids,
            skipped_summary_json=skipped_reasons,
            audit_log_id=audit_row.id,
        )
        db.commit()

    return OrganizationGovernanceApplyToOpenBatchRunsResponse(
        dry_run=payload.dry_run,
        target_value=target_value,
        eligible_count=eligible_count,
        updated_count=updated_count,
        skipped_count=skipped_count,
        affected_run_ids=affected_run_ids,
        skipped_reasons=skipped_reasons,
        caveat=CAVEAT_APPLY_TO_OPEN_BATCH_RUNS,
    )


