def _create_signing_key(
    db: Session,
    *,
    organization_id: uuid.UUID,
    purpose: str,
    actor_user_id: uuid.UUID | None,
    reason: str,
) -> OrganizationInternalSigningKey:
    existing = _list_signing_keys(db, organization_id=organization_id, purpose=purpose)
    row = OrganizationInternalSigningKey(
        organization_id=organization_id,
        key_id=_next_signing_key_id(existing_keys=existing, organization_id=organization_id),
        algorithm=GOVERNANCE_EVIDENCE_MANIFEST_SIGNATURE_ALGORITHM,
        status="active",
        purpose=purpose,
        created_by_user_id=actor_user_id,
        activated_at=_normalize_utc(datetime.now(timezone.utc)),
        metadata_json={"reason": reason},
    )
    db.add(row)
    db.flush()
    return row


def _get_or_create_active_signing_key(
    db: Session,
    *,
    organization_id: uuid.UUID,
    purpose: str,
    actor_user_id: uuid.UUID | None,
    request: Request | None,
    create_reason: str,
) -> tuple[OrganizationInternalSigningKey, bool]:
    active = _active_signing_key(db, organization_id=organization_id, purpose=purpose)
    if active is not None:
        return active, False
    created = _create_signing_key(
        db,
        organization_id=organization_id,
        purpose=purpose,
        actor_user_id=actor_user_id,
        reason=create_reason,
    )
    AuditService(db).write_audit_log(
        action=INTERNAL_SIGNING_KEY_CREATED_ACTION,
        entity_type="organization_internal_signing_key",
        entity_id=created.id,
        organization_id=organization_id,
        actor_user_id=actor_user_id,
        after_json={
            "key_id": created.key_id,
            "algorithm": created.algorithm,
            "status": created.status,
            "purpose": created.purpose,
        },
        metadata_json={"source": "api", "reason": create_reason, "auto_created": True},
        ip_address=request.client.host if request and request.client else None,
        user_agent=request.headers.get("user-agent") if request else None,
    )
    return created, True


