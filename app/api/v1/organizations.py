def summarize_organization_governance_manifest_verifications(
    db: Session = Depends(get_db),
    organization: Organization = Depends(get_current_organization),
    _=Depends(require_permission("org:read")),
) -> OrganizationGovernanceManifestVerificationSummaryResponse:
    total_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id
        )
    ).scalar_one()
    trusted_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id,
            OrganizationGovernanceManifestVerificationEvent.trusted.is_(True),
        )
    ).scalar_one()
    failed_hash_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id,
            OrganizationGovernanceManifestVerificationEvent.valid_hash.is_(False),
        )
    ).scalar_one()
    failed_signature_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id,
            OrganizationGovernanceManifestVerificationEvent.valid_signature.is_(False),
        )
    ).scalar_one()
    legacy_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id,
            OrganizationGovernanceManifestVerificationEvent.legacy_verification.is_(True),
        )
    ).scalar_one()
    revoked_key_verifications = db.execute(
        select(func.count(OrganizationGovernanceManifestVerificationEvent.id)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id,
            OrganizationGovernanceManifestVerificationEvent.key_status == "revoked",
        )
    ).scalar_one()
    latest_verification_at = db.execute(
        select(func.max(OrganizationGovernanceManifestVerificationEvent.verified_at)).where(
            OrganizationGovernanceManifestVerificationEvent.organization_id == organization.id
        )
    ).scalar_one()
    trusted_count = int(trusted_verifications or 0)
    total_count = int(total_verifications or 0)
    return OrganizationGovernanceManifestVerificationSummaryResponse(
        total_verifications=total_count,
        trusted_verifications=trusted_count,
        untrusted_verifications=max(total_count - trusted_count, 0),
        failed_hash_verifications=int(failed_hash_verifications or 0),
        failed_signature_verifications=int(failed_signature_verifications or 0),
        legacy_verifications=int(legacy_verifications or 0),
        revoked_key_verifications=int(revoked_key_verifications or 0),
        latest_verification_at=latest_verification_at,
        caveat=GOVERNANCE_EVIDENCE_MANIFEST_VERIFICATION_CAVEAT,
    )


