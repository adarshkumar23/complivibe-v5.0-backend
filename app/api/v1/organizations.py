def _get_governance_history_rows(
    db: Session,
    *,
    organization_id: uuid.UUID,
    event_type: str | None = None,
    setting_key: str | None = None,
    from_version: int | None = None,
    to_version: int | None = None,
    order_desc: bool = True,
    limit: int | None = None,
    offset: int = 0,
) -> list[OrganizationGovernanceSettingHistory]:
    stmt = select(OrganizationGovernanceSettingHistory).where(
        OrganizationGovernanceSettingHistory.organization_id == organization_id
    )
    if event_type:
        stmt = stmt.where(OrganizationGovernanceSettingHistory.event_type == event_type)
    if setting_key:
        stmt = stmt.where(OrganizationGovernanceSettingHistory.setting_key == setting_key)
    if from_version is not None:
        stmt = stmt.where(OrganizationGovernanceSettingHistory.version >= from_version)
    if to_version is not None:
        stmt = stmt.where(OrganizationGovernanceSettingHistory.version <= to_version)
    if order_desc:
        stmt = stmt.order_by(OrganizationGovernanceSettingHistory.created_at.desc(), OrganizationGovernanceSettingHistory.version.desc())
    else:
        stmt = stmt.order_by(OrganizationGovernanceSettingHistory.version.asc(), OrganizationGovernanceSettingHistory.created_at.asc())
    if limit is not None:
        stmt = stmt.limit(limit)
    if offset:
        stmt = stmt.offset(offset)
    return db.execute(stmt).scalars().all()


def _build_governance_timeline_entries(
    db: Session,
    *,
    organization_id: uuid.UUID,
    history_rows: list[OrganizationGovernanceSettingHistory],
) -> list[OrganizationGovernanceTimelineEntry]:
    referenced_audit_ids = {row.audit_log_id for row in history_rows if row.audit_log_id is not None}
    audit_rows = db.execute(
        select(AuditLog).where(
            AuditLog.organization_id == organization_id,
            AuditLog.action.in_(GOVERNANCE_AUDIT_ACTIONS),
        )
    ).scalars().all()

    entries: list[OrganizationGovernanceTimelineEntry] = []
    for row in history_rows:
        summary = f"{row.event_type} ({row.setting_key})"
        if row.reason:
            summary = f"{summary}: {row.reason}"
        entries.append(
            OrganizationGovernanceTimelineEntry(
                timestamp=_normalize_utc(row.created_at),
                event_type=row.event_type,
                actor_user_id=row.changed_by_user_id,
                summary=summary,
                source="history",
                before_json=row.before_json,
                after_json=row.after_json,
            )
        )

    for row in audit_rows:
        if row.id in referenced_audit_ids:
            continue
        summary = row.action
        reason = row.metadata_json.get("reason") if isinstance(row.metadata_json, dict) else None
        if reason:
            summary = f"{summary}: {reason}"
        entries.append(
            OrganizationGovernanceTimelineEntry(
                timestamp=_normalize_utc(row.created_at),
                event_type=row.action,
                actor_user_id=row.actor_user_id,
                summary=summary,
                source="audit_log",
                before_json=row.before_json,
                after_json=row.after_json,
            )
        )

    entries.sort(key=lambda item: item.timestamp, reverse=True)
    return entries


