@dataclass
class ExportBranding:
    organization_id: uuid.UUID
    company_display_name: str
    logo_url: str | None = None
    footer_text: str = "Generated by CompliVibe"
    primary_color_hex: str = "#1F4B99"


@dataclass
class ExportSection:
    title: str
    rows: list[tuple[str, str]] = field(default_factory=list)
    paragraphs: list[str] = field(default_factory=list)
    items: list[str] = field(default_factory=list)


@dataclass
class ExportDocumentContent:
    organization_id: uuid.UUID
    title: str
    subtitle: str | None
    generated_at: datetime
    entity_type: str
    entity_id: uuid.UUID | None
    report_type: str | None
    branding: ExportBranding
    sections: list[ExportSection]


class ExportContentBuilder:
    def __init__(self, db: Session):
        self.db = db

    @staticmethod
    def _now() -> datetime:
        return datetime.now(UTC)

    def _org_or_404(self, org_id: uuid.UUID) -> Organization:
        org = self.db.get(Organization, org_id)
        if org is None:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Organization not found")
        return org

    def _branding(self, org_id: uuid.UUID) -> ExportBranding:
        org = self._org_or_404(org_id)
        row = (
            self.db.query(OrganizationExportSetting)
            .filter(OrganizationExportSetting.organization_id == org_id)
            .first()
        )
        if row is None:
            return ExportBranding(
                organization_id=org_id,
                company_display_name=org.name,
            )
        return ExportBranding(
            organization_id=org_id,
            company_display_name=row.company_display_name or org.name,
            logo_url=row.logo_url,
            footer_text=row.footer_text or "Generated by CompliVibe",
            primary_color_hex=row.primary_color_hex or "#1F4B99",
        )

    def _owner_insight(self, owner_user_id: uuid.UUID | None) -> str | None:
        """Flag when an entity's owner has been deactivated elsewhere in the platform.

        Exports are frequently pulled well after the fact (audits, board packets); a
        deactivated owner means nobody is actively accountable for the item and that
        needs to be visible on the document itself, not just inferred by a reader who
        happens to also check the user directory.
        """
        if owner_user_id is None:
            return "No owner assigned - accountability gap."
        owner = self.db.get(User, owner_user_id)
        if owner is None:
            return "Assigned owner no longer exists in this organization."
        if not owner.is_active or owner.status != "active":
            return f"Assigned owner ({owner.email}) is deactivated - reassign before relying on this record."
        return None

    @staticmethod
    def _to_rows(payload: dict[str, Any], prefix: str = "") -> list[tuple[str, str]]:
        rows: list[tuple[str, str]] = []
        for key, value in payload.items():
            label = f"{prefix}{key}".replace("_", " ").title()
            if isinstance(value, dict):
                rows.extend(ExportContentBuilder._to_rows(value, prefix=f"{label} / "))
            elif isinstance(value, list):
                rows.append((label, ", ".join(str(item) for item in value)))
            else:
                rows.append((label, "" if value is None else str(value)))
        return rows

    def build_policy(self, org_id: uuid.UUID, policy_id: uuid.UUID) -> ExportDocumentContent:
        policy = CompliancePolicyService(self.db).require_policy_in_org(org_id, policy_id)
        sections = [
            ExportSection(
                title="Policy",
                rows=[
                    ("Title", policy.title),
                    ("Description", policy.description or ""),
                    ("Policy Type", policy.policy_type),
                    ("Status", policy.status),
                    ("Version", policy.version),
                    ("Content URL", policy.content_url or ""),
                    ("Owner User ID", str(policy.owner_user_id)),
                    ("Review Due Date", str(policy.review_due_date) if policy.review_due_date else ""),
                    ("Effective Date", str(policy.effective_date) if policy.effective_date else ""),
                    ("Created At", policy.created_at.isoformat()),
                    ("Updated At", policy.updated_at.isoformat()),
                ],
            )
        ]

        insights: list[str] = []
        today = datetime.now(UTC).date()
        if policy.archived_at is not None:
            insights.append("This policy is archived - do not present as current guidance.")
        if policy.review_due_date is not None and policy.review_due_date < today and policy.archived_at is None:
            days_overdue = (today - policy.review_due_date).days
            insights.append(
                f"Review is overdue by {days_overdue} day(s) (due {policy.review_due_date}) - "
                "content may no longer reflect current practice."
            )
        if policy.status == "draft" and policy.effective_date is not None and policy.effective_date <= today:
            insights.append(
                f"Effective date ({policy.effective_date}) has passed but the policy is still in draft status."
            )
        owner_flag = self._owner_insight(policy.owner_user_id)
        if owner_flag:
            insights.append(owner_flag)
        if insights:
            sections.append(ExportSection(title="Insights", items=insights))

        return ExportDocumentContent(
            organization_id=org_id,
            title=f"Compliance Policy Export: {policy.title}",
            subtitle=None,
            generated_at=self._now(),
            entity_type="compliance_policy",
            entity_id=policy.id,
            report_type=None,
            branding=self._branding(org_id),
            sections=sections,
        )

    def build_control(self, org_id: uuid.UUID, control_id: uuid.UUID) -> ExportDocumentContent:
        control = ControlRepository(self.db).get_by_id(control_id)
        if control is None or control.organization_id != org_id:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Control not found")
        sections = [
            ExportSection(
                title="Control",
                rows=[
                    ("Title", control.title),
                    ("Description", control.description or ""),
                    ("Control Code", control.control_code or ""),
                    ("Control Type", control.control_type),
                    ("Status", control.status),
                    ("Criticality", control.criticality),
                    ("Owner User ID", str(control.owner_user_id) if control.owner_user_id else ""),
                    ("Testing Procedure", control.testing_procedure or ""),
                    ("Implementation Notes", control.implementation_notes or ""),
                    ("Created At", control.created_at.isoformat()),
                    ("Updated At", control.updated_at.isoformat()),
                ],
            )
        ]

        insights: list[str] = []
        if control.criticality in {"high", "critical"} and control.status not in {"implemented", "effective"}:
            insights.append(
                f"Criticality is {control.criticality} but status is '{control.status}' - "
                "this control is not yet operating effectively."
            )
        if control.criticality in {"high", "critical"} and not (control.testing_procedure or "").strip():
            insights.append(
                f"Criticality is {control.criticality} but no testing procedure is documented."
            )
        owner_flag = self._owner_insight(control.owner_user_id)
        if owner_flag:
            insights.append(owner_flag)
        if insights:
            sections.append(ExportSection(title="Insights", items=insights))

        return ExportDocumentContent(
            organization_id=org_id,
            title=f"Control Export: {control.title}",
            subtitle=None,
            generated_at=self._now(),
            entity_type="control",
            entity_id=control.id,
            report_type=None,
            branding=self._branding(org_id),
            sections=sections,
        )

    def build_risk(self, org_id: uuid.UUID, risk_id: uuid.UUID) -> ExportDocumentContent:
        risk = RiskRepository(self.db).get_by_id(risk_id)
        if risk is None or risk.organization_id != org_id:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Risk not found")
        sections = [
            ExportSection(
                title="Risk",
                rows=[
                    ("Title", risk.title),
                    ("Description", risk.description or ""),
                    ("Category", risk.category),
                    ("Status", risk.status),
                    ("Severity", risk.severity),
                    ("Likelihood", str(risk.likelihood)),
                    ("Impact", str(risk.impact)),
                    ("Inherent Score", str(risk.inherent_score)),
                    ("Treatment Strategy", risk.treatment_strategy),
                    ("Owner User ID", str(risk.owner_user_id) if risk.owner_user_id else ""),
                    ("Created At", risk.created_at.isoformat()),
                    ("Updated At", risk.updated_at.isoformat()),
                ],
            )
        ]

        insights: list[str] = []
        now = datetime.now(UTC)
        if risk.review_due_at is not None:
            review_due = risk.review_due_at if risk.review_due_at.tzinfo else risk.review_due_at.replace(tzinfo=UTC)
            if review_due < now and risk.status not in {"closed", "accepted"}:
                insights.append(
                    f"Risk review is overdue (was due {review_due.isoformat()}) - "
                    "scoring may be stale."
                )
        if risk.residual_score is None:
            insights.append("Residual risk has not been assessed - only inherent risk is quantified here.")
        elif risk.residual_score >= risk.inherent_score:
            insights.append(
                "Residual score is not lower than inherent score - documented treatment has not "
                "measurably reduced this risk."
            )
        if (
            risk.treatment_strategy == "accept"
            and risk.severity in {"high", "critical"}
            and not risk.residual_risk_acceptable
        ):
            insights.append(
                f"Severity is {risk.severity} and treatment is 'accept' but there is no recorded "
                "acceptance sign-off (residual_risk_acceptable is not set)."
            )
        owner_flag = self._owner_insight(risk.owner_user_id)
        if owner_flag:
            insights.append(owner_flag)
        if insights:
            sections.append(ExportSection(title="Insights", items=insights))

        return ExportDocumentContent(
            organization_id=org_id,
            title=f"Risk Export: {risk.title}",
            subtitle=None,
            generated_at=self._now(),
            entity_type="risk",
            entity_id=risk.id,
            report_type=None,
            branding=self._branding(org_id),
            sections=sections,
        )

    def build_vendor(self, org_id: uuid.UUID, vendor_id: uuid.UUID) -> ExportDocumentContent:
        vendor = VendorService(self.db).require_vendor_in_org(org_id, vendor_id)
        sections = [
            ExportSection(
                title="Vendor",
                rows=[
                    ("Name", vendor.name),
                    ("Description", vendor.description or ""),
                    ("Vendor Type", vendor.vendor_type),
                    ("Website", vendor.website or ""),
                    ("Primary Contact Name", vendor.primary_contact_name or ""),
                    ("Primary Contact Email", vendor.primary_contact_email or ""),
                    ("Risk Tier", vendor.risk_tier),
                    ("Status", vendor.status),
                    ("Owner User ID", str(vendor.owner_user_id)),
                    ("Created At", vendor.created_at.isoformat()),
                    ("Updated At", vendor.updated_at.isoformat()),
                ],
            )
        ]

        insights: list[str] = []
        if vendor.risk_tier == "not_assessed" and (vendor.data_access or vendor.processes_personal_data):
            insights.append(
                "Vendor has data access or processes personal data but has not been risk-assessed."
            )
        if vendor.nth_party_risk_flag:
            severity = vendor.nth_party_risk_severity or "unspecified"
            insights.append(f"Nth-party risk signal flagged (severity: {severity}) - review sub-processor chain.")
        if vendor.sub_processor and vendor.risk_tier in {"not_assessed", "low"}:
            insights.append(
                "Vendor relies on sub-processors but is rated low/not-assessed risk - "
                "verify sub-processor due diligence coverage."
            )
        owner_flag = self._owner_insight(vendor.owner_user_id)
        if owner_flag:
            insights.append(owner_flag)
        if insights:
            sections.append(ExportSection(title="Insights", items=insights))

        return ExportDocumentContent(
            organization_id=org_id,
            title=f"Vendor Export: {vendor.name}",
            subtitle=None,
            generated_at=self._now(),
            entity_type="vendor",
            entity_id=vendor.id,
            report_type=None,
            branding=self._branding(org_id),
            sections=sections,
        )

