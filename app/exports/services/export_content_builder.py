    def build_audit_evidence_package(
        self,
        org_id: uuid.UUID,
        audit_id: uuid.UUID,
        framework_id: uuid.UUID | None = None,
    ) -> ExportDocumentContent:
        engagement = (
            self.db.query(AuditEngagement)
            .filter(
                AuditEngagement.organization_id == org_id,
                AuditEngagement.id == audit_id,
                AuditEngagement.deleted_at.is_(None),
            )
            .first()
        )
        if engagement is None:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Audit engagement not found")

        scoped_framework_id: uuid.UUID | None = framework_id
        if scoped_framework_id is None and engagement.scope_framework_ids:
            try:
                scoped_framework_id = uuid.UUID(str(engagement.scope_framework_ids[0]))
            except (ValueError, TypeError):
                scoped_framework_id = None

        framework_name: str | None = None
        if scoped_framework_id is not None:
            fw = self.db.query(Framework).filter(Framework.id == scoped_framework_id).first()
            framework_name = fw.name if fw else None

        # Obligations belong to the (org-agnostic) framework catalog, not to a per-org row --
        # scope by the org's ACTIVE framework(s) rather than requiring an
        # OrganizationObligationState row, which only exists once a user has individually
        # reviewed that specific obligation (most obligations under a freshly-activated
        # framework never get one, and previously made this export silently show zero
        # obligations/controls/evidence despite real coverage existing).
        obligation_query = (
            self.db.query(Obligation)
            .join(OrganizationFramework, OrganizationFramework.framework_id == Obligation.framework_id)
            .filter(
                OrganizationFramework.organization_id == org_id,
                OrganizationFramework.status == "active",
            )
        )
        if scoped_framework_id is not None:
            obligation_query = obligation_query.filter(Obligation.framework_id == scoped_framework_id)
        obligations = obligation_query.order_by(Obligation.reference_code.asc(), Obligation.title.asc()).all()

        sections: list[ExportSection] = []
        total_obligations = len(obligations)
        obligations_with_verified = 0
        total_controls = 0
        controls_with_verified = 0
        total_evidence_items = 0

        for obligation in obligations:
            linked_controls = (
                self.db.query(Control)
                .outerjoin(
                    ControlObligationMapping,
                    (ControlObligationMapping.organization_id == org_id)
                    & (ControlObligationMapping.control_id == Control.id)
                    & (ControlObligationMapping.obligation_id == obligation.id)
                    & (ControlObligationMapping.status == "active"),
                )
                .filter(Control.organization_id == org_id)
                .filter(
                    (Control.obligation_id == obligation.id) | (ControlObligationMapping.id.is_not(None))
                )
                .order_by(Control.title.asc())
                .all()
            )

            control_lines: list[str] = []
            has_verified_for_obligation = False
            for control in linked_controls:
                total_controls += 1
                evidence_rows = (
                    self.db.query(EvidenceItem)
                    .join(EvidenceControlLink, EvidenceControlLink.evidence_item_id == EvidenceItem.id)
                    .filter(
                        EvidenceControlLink.organization_id == org_id,
                        EvidenceControlLink.control_id == control.id,
                        EvidenceControlLink.link_status == "active",
                        EvidenceItem.organization_id == org_id,
                        EvidenceItem.review_status == "verified",
                    )
                    .order_by(EvidenceItem.updated_at.desc())
                    .all()
                )
                if evidence_rows:
                    has_verified_for_obligation = True
                    controls_with_verified += 1
                total_evidence_items += len(evidence_rows)
                evidence_lines = [
                    (
                        f"- {item.title} | source={item.source} | "
                        f"verified_at={(item.reviewed_at.isoformat() if item.reviewed_at else item.updated_at.isoformat())}"
                    )
                    for item in evidence_rows
                ]
                control_lines.append(
                    f"Control: {control.title} | status={control.status} | verified_evidence={len(evidence_rows)}"
                )
                control_lines.extend(evidence_lines)

            if has_verified_for_obligation:
                obligations_with_verified += 1

            obligation_ref = obligation.reference_code or str(obligation.id)
            sections.append(
                ExportSection(
                    title=f"{obligation_ref} - {obligation.title}",
                    items=control_lines,
                )
            )

        coverage_pct = round((obligations_with_verified / total_obligations) * 100, 2) if total_obligations else 0.0

        summary_rows = [
            ("Audit Title", engagement.title),
            ("Audit Status", engagement.status),
            ("Framework", framework_name or "All"),
            ("Total Obligations", str(total_obligations)),
            ("Obligations With Verified Evidence", str(obligations_with_verified)),
            ("Total Controls", str(total_controls)),
            ("Controls With Verified Evidence", str(controls_with_verified)),
            ("Total Evidence Items", str(total_evidence_items)),
            ("Coverage Percent", f"{coverage_pct}"),
        ]
        sections.insert(0, ExportSection(title="Evidence Package Summary", rows=summary_rows))

        return ExportDocumentContent(
            organization_id=org_id,
            title=f"Audit Evidence Package: {engagement.title}",
            subtitle=f"Audit ID: {engagement.id}",
            generated_at=self._now(),
            entity_type="audit_engagement",
            entity_id=engagement.id,
            report_type="audit_evidence_package",
            branding=self._branding(org_id),
            sections=sections,
        )
